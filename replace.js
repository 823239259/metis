var tel = '(010)12345678 (022)23456798 (011)12356895';
var pattern = /\((\d{3})\)(\d{8})/g;

tel.replace(pattern, function () {
    // 参数1 当前匹配模式字符串
    // 参数2 -> 参数n 当前匹配模式字符串 的 n个匹配子集
    // 参数-2 当前匹配模式字符串起始位置
    // 参数-1 被匹配源字符串
});
// replace 执行次数 为 匹配模式字符串数量
// 无 /g 执行一次 replace 首个匹配模式字符串

tel.replace(pattern, '$1-$2'); // '010-12345678 022-23456798 011-12356895'
// () 内 为匹配子集 由 $num 指代
// $$ 指代 $符本身