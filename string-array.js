/** Array **/
/**
 * add: push(...) -> last  unshift(...) -> first
 * remove: pop() -> last  shift() -> first
 * splice: splice(index, num, ...)
 * sort: reverse()  sort()
 * 改变原数组
 *
 * slice: slice(start, end)
 * concat: concat(...)
 * index: indexOf(value, fromindex)  lastIndexOf(value, fromindex)
 * join: join(separator) -> String
 * 不改变原数组
 */


/** String **/
/**
 * concat: concat(...)
 * index: indexOf(value, fromindex) -> index [无查询结果返回 -1]  charAt(index) -> value [不接受负数]  lastIndexOf(value, fromindex) [无查询结果返回 -1]
 * slice: substr(start, length)  substring(pos1, pos2)  slice(start, end)
 * split: split(separator, num) -> Array
 * change: toUpperCase()  toLowerCase()
 * 不改变原字符串
 *
 * pattern: match  replace  search
 */